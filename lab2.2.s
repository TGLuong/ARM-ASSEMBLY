	AREA RESET, DATA,READONLY
	EXPORT __VECTORS
__VECTORS
	DCD 0x20001000
	DCD RESET_HANDLER
ARR	DCW	10,2,4,-3,11,-7
EOA	DCW 0
	
	AREA MYCODE, CODE, READONLY
	EXPORT RESET_HANDLER
	ENTRY
RESET_HANDLER
	LDR R0,=ARR
	LDR R1,=EOA
	LDRSH R3,[R0];THANH THI GIA TRI NHO NHAT
	LDRSH R4,[R0];THANH GHI GIA TRI LON NHAT
LOOP
	CMP R1,R0
	BEQ DONE
	LDRSH R2,[R0],#2
	CMP R2,R3
	MOVLT R3,R2
	CMP R2,R4
	MOVGT R4,R2
	B LOOP
	
DONE	

	SWI &11
	END