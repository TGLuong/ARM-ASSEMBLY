	AREA RESET, DATA, READONLY
	EXPORT __VECTORS
__VECTORS
	DCD 0x20001000
	DCD RESET_HANDLER
MATRIX
	DCD 1,2,3,4
	DCD -1,-2,-3,-4
	DCD -3,2,-4,4
EOM DCD 0
	
	AREA MYCODE, CODE, READONLY
	EXPORT RESET_HANDLER
RESET_HANDLER
	LDR R0, = MATRIX
	LDR R1, = EOM
	
LOOP
	CMP R0,R1
	BEQ DONE
	LDR R2,[R0],#4
	CMP R2,#0
	ADDLT R3,#1
	ADDGT R4,#1
	B LOOP
DONE
	
	
	SWI &11
	END