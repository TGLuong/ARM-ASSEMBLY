	AREA RESET, DATA, READONLY
	EXPORT __VECTORS
__VECTORS
	DCD 0x20001000
	DCD RESET_HANDLER
MATRIX
	DCD 1,2,3,4
	DCD 5,6,7,8
	DCD 9,10,11,12
	DCD 13,14,15,16
EOM DCD 0
	
	AREA MYCODE, CODE, READONLY
	EXPORT RESET_HANDLER
	ENTRY
RESET_HANDLER
	LDR R0,=MATRIX
	LDR R1,=EOM
LOOP1
	CMP R0,R1
	BEQ DONE1
	LDR R2,[R0],#4
	MOV R3,R2
LOOP2
	CMP R3,#0
	BLT DONE2
	ADDEQ R4,R2
	SUB R3,#5
	B LOOP2
DONE2
	B LOOP1
DONE1
	SWI &11
	END