	AREA RESET, DATA, READONLY
	EXPORT __VECTORS
__VECTORS
	DCD 0x20001000
	DCD RESET_HANDLER
N	DCD 4
		
	AREA SUMAREA, CODE, READONLY
SUM PROC
	PUSH {LR}
	MOV R1,R0
SUMLOOP
	CMP R1,#0
	BEQ ENDSUMLOOP
	ADD R2,R1
	SUB R1,#1
	B SUMLOOP
ENDSUMLOOP
	POP {LR}
	BX LR
	ENDP
	
	AREA MAIN, CODE, READONLY
	EXPORT RESET_HANDLER
RESET_HANDLER
	LDR R0,N
	BL SUM
	SWI &11
	END