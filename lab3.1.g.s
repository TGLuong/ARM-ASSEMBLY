	AREA RESET, DATA,READONLY
	EXPORT __VECTORS
__VECTORS
	DCD 0x20001000
	DCD RESET_HANDLER
M	DCD 12
N	DCD 9
		
	AREA UCLN_AREA , CODE, READONLY
UCLN PROC
	PUSH {LR}
	MOV R2,R0
	MOV R3,R1
UCLNLOOP
	CMP R2,R3
	BEQ ENDUCLNLOOP
	SUBLT R3,R2
	SUBGT R2,R3
	B UCLNLOOP
ENDUCLNLOOP
	MOV R4,R3; KET QUA
	POP {LR}
	ENDP
	
	AREA MAIN, CODE, READONLY
	EXPORT RESET_HANDLER
RESET_HANDLER
	LDR R0, M
	LDR R1, N
	BL UCLN
	SWI &11
	END