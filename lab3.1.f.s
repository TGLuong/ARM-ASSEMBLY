	AREA RESET, DATA, READONLY
	EXPORT __VECTORS
__VECTORS
	DCD 0x20001000
	DCD RESET_HANDLER
X	DCD 2
N	DCD 3


	AREA CHUOILT_AREA, CODE, READONLY
CHUOILT
	PUSH {LR}
	MOV R2,R1 ; INDEX : N
	MOV R3,R0
	MOV R4,#1; THANH GHI KET QUA
CHUOILTLOOP
	CMP R2,#1
	BEQ ENDCHUOILTLOOP
	MUL R3,R0
	ADD R4,R3
	SUB R2,#1
	B CHUOILTLOOP
ENDCHUOILTLOOP
	POP {LR}
	BX LR
	ENDP


	AREA MAIN, CODE, READONLY
	EXPORT RESET_HANDLER
RESET_HANDLER
	LDR R0, X
	LDR R1, N
	CMP R1,#2
	BLT STOP
	BL CHUOILT
STOP
	SWI &11
	END