	AREA RESET, DATA, READONLY
	EXPORT __VECTORS
__VECTORS
	DCD 0x20001000
	DCD RESET_HANDLER
N	DCD 20
	
	AREA TONGNAM_AREA, CODE, READONLY
TONGNAM
	PUSH{LR}
	MOV R1,R0
	MOV R2,#0 
	MOV R3,#0 ; KET QUA
TONGNAMLOOP
	ADD R2,#5
	CMP R2,R1
	BGT ENDTONGNAMLOOP
	ADD R3,R2
	B TONGNAMLOOP
ENDTONGNAMLOOP
	POP{LR}
	BX LR
	ENDP
	
	AREA MAIN, CODE, READONLY
	EXPORT RESET_HANDLER
RESET_HANDLER
	LDR R0, N
	BL TONGNAM
	SWI &11
	END