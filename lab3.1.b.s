	AREA RESET, DATA, READONLY
	EXPORT __VECTORS
__VECTORS
	DCD 0x20001000
	DCD RESET_HANDLER
N	DCD 6	
	
	AREA CHANLE_AREA, CODE , READONLY
CHANLE
	PUSH {LR}
	MOV R1,R0
CHANLELOOP
	CMP R1, #0
	BEQ ENDCHANLELOOP
	MOV R2,R1
	AND R2,#1
	CMP R2,#1
	ADDEQ R3,R1 ;R3 THANH GHI LE
	ADDNE R4,R1 ;R4 THANH GHI CHAN
	SUB R1,#1
	B CHANLELOOP
ENDCHANLELOOP	
	POP {LR}
	BX LR
	ENDP
	
	AREA MAIN, CODE, READONLY
	EXPORT RESET_HANDLER
RESET_HANDLER	
	LDR R0,N
	BL CHANLE
	SWI &11
	END