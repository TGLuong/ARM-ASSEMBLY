	AREA RESET, DATA, READONLY
	EXPORT __VECTORS
__VECTORS
	DCD 0x20001000
	DCD RESET_HANDLER
X	DCD 2
N	DCD 3

	
	AREA POW_AREA, CODE, READONLY
POW	PROC
	PUSH {LR}
	MOV R3,R2; INDEX
	MOV R4,#1; THANH GHI KET QUA
POWLOOP
	CMP R3,#0
	BEQ ENDPOWLOOP
	MUL R4,R1
	SUB R3,#1
	B POWLOOP
ENDPOWLOOP
	POP {LR}
	BX LR
	ENDP
	
	AREA MAIN, CODE, READONLY
	EXPORT RESET_HANDLER
RESET_HANDLER
	LDR R1, X
	LDR R2, N
	BL POW
	SWI &11
	END