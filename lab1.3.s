	AREA RESET, DATA, READONLY
	EXPORT __VECTORS
__VECTORS
	DCD 0x20001000
	DCD RESET_HANDLER
N	DCD 4

	AREA MYCODE, CODE, READONLY
	EXPORT RESET_HANDLER
	ENTRY
RESET_HANDLER
	LDR R0, N
	CMP R0, #0
	BLO DONE
	MOV R4,#1
	BEQ DONE
LOOP
	CMP R0,#1
	BEQ DONE
	MUL R4,R0
	SUB R0,#1
	B LOOP
DONE
	SWI &11
	END