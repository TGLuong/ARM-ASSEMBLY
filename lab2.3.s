	AREA RESET, DATA, READONLY
	EXPORT __VECTORS
__VECTORS
	DCD 0x20001000
	DCD RESET_HANDLER
ARR	DCW 3,4,5,8,12,23,17,2
EOA	DCW 0	
	AREA MYCODE, CODE, READONLY
	EXPORT RESET_HANDLER
	ENTRY
RESET_HANDLER
	LDR R0, =ARR
	LDR R1, =EOA
	MOV R2,#0; INDEX
	MOV R3,#0; COUNT
	MOV R4,#0; SUM
	MOV R5,#0; AVG
LOOP1
	CMP R0,R1
	BEQ DONE1
	LDRSH R2,[R0],#2
	ADD R4,R2
	ADD R3,#1
	B LOOP1
DONE1
LOOP2
	CMP R4,R3
	BLT DONE2
	SUB R4,R3
	ADD R5,#1
	B LOOP2
DONE2

	
	
	SWI &11
	END