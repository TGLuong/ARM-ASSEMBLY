	AREA RESET, DATA, READONLY
	EXPORT __VECTORS
__VECTORS
	DCD 0x20001000
	DCD RESET_HANDLER
MATRIX
	DCD 1,2,3,4,5
	DCD 2,3,4,5,6
	DCD 7,8,9,6,4
EOM	DCD 0

	
	AREA MYCODE, CODE, READONLY
	EXPORT RESET_HANDLER
	ENTRY
RESET_HANDLER
	LDR R0,=MATRIX
	LDR R1,=EOM
LOOP
	CMP R0,R1
	BEQ DONE
	LDR R2,[R0],#4
	ADD R3,R2
	B LOOP
DONE
	SWI &11
	END